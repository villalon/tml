package tml.vectorspace.operations.results;

/**
 * @author Jorge Villalon
 *
 */
public class TagCloudsResult extends AbstractResult {

	String term;
	double weight;

	/**
	 * @param term
	 * @param weight
	 */
	public TagCloudsResult(String term, double weight) {
		super();
		this.term = term;
		this.weight = weight;
	}

	/**
	 * @param weight the weight to set
	 */
	public void setWeight(double weight) {
		this.weight = weight;
	}

	/**
	 * @return the weight
	 */
	public double getWeight() {
		return weight;
	}

	/**
	 * @param term the term to set
	 */
	public void setTerm(String term) {
		this.term = term;
	}

	/**
	 * @return the term
	 */
	public String getTerm() {
		return term;
	}
}
